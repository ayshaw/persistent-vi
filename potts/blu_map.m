function cmap = blu_map()
% Dark blue|magenta divergent colormap
    C = [37 50 89;...
    31 78 136;...
    250 250 250; ...
    226 97 164;...
    66 18 75;] / 255;
    C = flipdim(C,1);
    trim = 0.3;
    cmap = interp1([1 1.5 2 2.5 3], C,...
                   linspace(1 + trim, 3-trim,128), 'linear');
end
